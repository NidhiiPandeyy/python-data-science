# Arithmatic Operators

x = 15
y = 4

print('x + y =',x+y)
print('x - y =',x-y)
print('x * y =',x*y)
print('x / y =',x/y)
print('x // y =',x//y)
print('x ** y =',x**y)

# Logical Operatorx 

print(x > 10 and x < 20)
print(x > 10 or x < 20)
print(not(x > 10 or x < 20))

# Bitwise operators

a=12 
b= 25
print(f"a&b = {a&b}") #bitwise and
print(f"a|b = {a|b}") #bitwise or
print(f"a^b = {a^b}") #bitwise xor
print(f"~35= {~35}")  #bitwise compliment
print(f"~-12= {~-12}")


